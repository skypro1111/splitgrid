// Writes build/icon.svg as a full-bleed macOS-style "squircle" (continuous,
// Liquid-Glass corners — not a plain rounded rect), with the "S" (< over >)
// cut clean through it. The squircle geometry uses the figma-squircle algorithm
// (Apple iOS/macOS look: corner radius ~22.37% of the side, smoothing 0.6).
//
// Run:  node scripts/make-icon-svg.js   (then: npm run generate:icons)
const fs = require('node:fs');
const path = require('node:path');

const SIZE = 512;
const RADIUS = SIZE * 0.2237; // macOS/iOS squircle corner radius ratio
const SMOOTHING = 0.6;        // Apple-like continuous-corner smoothing

const rad = (d) => (d * Math.PI) / 180;

function cornerParams(cornerRadius, smoothing, budget) {
  const maxSmoothing = budget / cornerRadius - 1;
  smoothing = Math.min(smoothing, maxSmoothing);
  const p = Math.min((1 + smoothing) * cornerRadius, budget);
  const arcMeasure = 90 * (1 - smoothing);
  const arcSectionLength = Math.sin(rad(arcMeasure / 2)) * cornerRadius * Math.SQRT2;
  const angleAlpha = (90 - arcMeasure) / 2;
  const p3ToP4 = cornerRadius * Math.tan(rad(angleAlpha / 2));
  const angleBeta = 45 * smoothing;
  const c = p3ToP4 * Math.cos(rad(angleBeta));
  const d = c * Math.tan(rad(angleBeta));
  const b = (p - arcSectionLength - c - d) / 3;
  const a = 2 * b;
  return { a, b, c, d, p, arcSectionLength, cornerRadius };
}

function squirclePath(size, radius, smoothing) {
  const k = cornerParams(radius, smoothing, size / 2);
  const { a, b, c, d, p, arcSectionLength: s, cornerRadius: r } = k;
  return [
    `M ${size - p} 0`,
    `c ${a} 0 ${a + b} 0 ${a + b + c} ${d}`,
    `a ${r} ${r} 0 0 1 ${s} ${s}`,
    `c ${d} ${c} ${d} ${b + c} ${d} ${a + b + c}`,
    `L ${size} ${size - p}`,
    `c 0 ${a} 0 ${a + b} ${-d} ${a + b + c}`,
    `a ${r} ${r} 0 0 1 ${-s} ${s}`,
    `c ${-c} ${d} ${-(b + c)} ${d} ${-(a + b + c)} ${d}`,
    `L ${p} ${size}`,
    `c ${-a} 0 ${-(a + b)} 0 ${-(a + b + c)} ${-d}`,
    `a ${r} ${r} 0 0 1 ${-s} ${-s}`,
    `c ${-d} ${-c} ${-d} ${-(b + c)} ${-d} ${-(a + b + c)}`,
    `L 0 ${p}`,
    `c 0 ${-a} 0 ${-(a + b)} ${d} ${-(a + b + c)}`,
    `a ${r} ${r} 0 0 1 ${s} ${-s}`,
    `c ${c} ${-d} ${b + c} ${-d} ${a + b + c} ${-d}`,
    'Z',
  ].join(' ');
}

const d = squirclePath(SIZE, RADIUS, SMOOTHING);

const svg = `<!--
  SplitGrid Windows/Linux app-icon source (-> .ico / linux png). GENERATED by
  scripts/make-icon-svg.js — edit that script, not this file.
  (macOS uses the Icon Composer source public/logos/icon_macos.icon instead.)

  Full-bleed "squircle" (continuous corners), edge to edge with no margin, so it
  fills the tile at native size: a black tile with a solid white "S" (< over >),
  vertically centred — matching the macOS Liquid Glass icon's colour scheme.
-->
<svg width="${SIZE}" height="${SIZE}" viewBox="0 0 ${SIZE} ${SIZE}" fill="none" xmlns="http://www.w3.org/2000/svg">
  <path d="${d}" fill="black" />
  <text x="256" y="212.875" font-family="Arial, sans-serif" font-size="445"
    font-weight="900" fill="white" text-anchor="middle" dominant-baseline="middle">&lt;</text>
  <text x="256" y="382.875" font-family="Arial, sans-serif" font-size="445"
    font-weight="900" fill="white" text-anchor="middle" dominant-baseline="middle">&gt;</text>
</svg>
`;

fs.writeFileSync(path.resolve(__dirname, '..', 'build', 'icon.svg'), svg);
console.log('Wrote build/icon.svg (squircle)');
